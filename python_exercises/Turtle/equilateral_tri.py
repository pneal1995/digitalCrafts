from turtle import *
import matplotlib
import matplotlib.pyplot as plot

def equilateral_tri():
    for i in range(1):
        forward(320)
        left(120)
        forward(320)
        left(120)
        forward(320)
        left(120)
        

    plot.show()

if __name__ == "__main__":
    equilateral_tri()


